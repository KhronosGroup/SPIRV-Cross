	SPVC_CHECKED_CALL(spvc_compiler_options_set_uint(options, SPVC_COMPILER_OPTION_MSL_VERSION, 30000));
	SPVC_CHECKED_CALL(spvc_compiler_options_set_uint(options, SPVC_COMPILER_OPTION_MSL_DISPATCH_BASE, 1));
	SPVC_CHECKED_CALL(spvc_compiler_install_compiler_options(compiler, options));
	SPVC_CHECKED_CALL(spvc_compiler_compile(compiler, &result));

	SPVC_TEST_ASSERT(strstr(result, "struct spvMeshDispatch") != NULL);
	SPVC_TEST_ASSERT(strstr(result, "constant spvMeshDispatch& spvMeshDispatch [[buffer(") != NULL);
