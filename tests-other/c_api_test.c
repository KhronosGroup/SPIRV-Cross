static void test_msl_mesh_emulation(void)
{
	spvc_context context = NULL;
	spvc_parsed_ir ir = NULL;
	spvc_compiler compiler = NULL;
	spvc_compiler_options options = NULL;
	spvc_msl_shader_interface_var_2 shader_output;
	spvc_resources resources = NULL;
	const spvc_reflected_resource *outputs = NULL;
	const char *result = NULL;
	size_t output_count = 0;
	size_t i;

	SPVC_CHECKED_CALL(spvc_context_create(&context));
	spvc_context_set_error_callback(context, error_callback, NULL);
	SPVC_CHECKED_CALL(spvc_context_parse_spirv(
	    context, msl_mesh_output_spill_mesh_spv,
	    sizeof(msl_mesh_output_spill_mesh_spv) / sizeof(msl_mesh_output_spill_mesh_spv[0]), &ir));
	SPVC_CHECKED_CALL(spvc_context_create_compiler(context, SPVC_BACKEND_MSL, ir,
	                                              SPVC_CAPTURE_MODE_TAKE_OWNERSHIP, &compiler));
	SPVC_CHECKED_CALL(spvc_compiler_create_compiler_options(compiler, &options));
	SPVC_CHECKED_CALL(spvc_compiler_options_set_uint(options, SPVC_COMPILER_OPTION_MSL_VERSION, 30000));
	SPVC_CHECKED_CALL(spvc_compiler_options_set_uint(options,
	                                                SPVC_COMPILER_OPTION_MSL_MESH_SHADER_EMULATION, 1));
	SPVC_CHECKED_CALL(spvc_compiler_install_compiler_options(compiler, options));
	spvc_msl_shader_interface_var_init_2(&shader_output);
	shader_output.builtin = SpvBuiltInLayer;
	shader_output.vecsize = 1;
	shader_output.rate = SPVC_MSL_SHADER_VARIABLE_RATE_PER_PRIMITIVE;
	SPVC_CHECKED_CALL(spvc_compiler_msl_add_shader_output_2(compiler, &shader_output));
	shader_output.builtin = SpvBuiltInViewportIndex;
	SPVC_CHECKED_CALL(spvc_compiler_msl_add_shader_output_2(compiler, &shader_output));
	SPVC_CHECKED_CALL(spvc_compiler_create_shader_resources(compiler, &resources));
	SPVC_CHECKED_CALL(spvc_resources_get_resource_list_for_type(resources, SPVC_RESOURCE_TYPE_STAGE_OUTPUT,
	                                                           &outputs, &output_count));
	SPVC_CHECKED_CALL(spvc_compiler_compile(compiler, &result));

	SPVC_TEST_ASSERT(strstr(result, "kernel void main0(") != NULL);
	SPVC_TEST_ASSERT(strstr(result, "[[mesh]] void main0_replay(") != NULL);
	SPVC_TEST_ASSERT(strstr(result, "uint gl_Layer [[render_target_array_index]];") != NULL);
	SPVC_TEST_ASSERT(strstr(result, "uint gl_ViewportIndex [[viewport_array_index]];") != NULL);
	SPVC_TEST_ASSERT(strstr(result, "spvP.gl_Layer = 0u;") != NULL);
	SPVC_TEST_ASSERT(strstr(result, "spvP.gl_ViewportIndex = 0u;") != NULL);
	SPVC_TEST_ASSERT(spvc_compiler_msl_needs_output_buffer(compiler));
	SPVC_TEST_ASSERT(spvc_compiler_msl_get_mesh_output_buffer_size(compiler) == 128);
	SPVC_TEST_ASSERT(spvc_compiler_msl_get_mesh_output_buffer_alignment(compiler) == 16);
	SPVC_TEST_ASSERT(spvc_compiler_msl_get_mesh_output_threadgroup_size(compiler) == 0);
	SPVC_TEST_ASSERT(output_count > 0);
	for (i = 0; i < output_count; i++)
		SPVC_TEST_ASSERT(spvc_compiler_msl_get_mesh_output_buffer_offset(compiler, outputs[i].id) != ~0u);
	SPVC_TEST_ASSERT(spvc_compiler_msl_get_mesh_output_buffer_offset(compiler, 0) == ~0u);

	spvc_context_destroy(context);
}

